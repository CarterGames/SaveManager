using System;
using UnityEngine;

namespace CarterGames.Assets.SaveManager
{
    /// <summary>
    /// A base class for a save value.
    /// </summary>
    [Serializable]
    public abstract class SaveValueBase
    {
        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Fields
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        /// <summary>
        /// The key for the save value.
        /// </summary>
        [HideInInspector] public string key;

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Properties
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        /// <summary>
        /// The value of the save value as an object.
        /// </summary>
        public abstract object ValueObject { get; set; }

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Methods
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        /// <summary>
        /// Reset the value on call.
        /// </summary>
        public abstract void ResetValue();
        
        
        /// <summary>
        /// Assign the save value from 
        /// </summary>
        /// <param name="value">The value to assign from.</param>
        public abstract void AssignFromObject(SaveValueBase value);
    }
}